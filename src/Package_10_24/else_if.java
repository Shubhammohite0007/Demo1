package Package_10_24;

public class else_if {

	public static void main(String[] args) {
		int a=100;
		int b=100;
		
		if(a>b) {
			System.out.println("A is greater");
		}
		else if(a<b) {
			System.out.println("A is greater");
		}
		else if(a!=b) {
			System.out.println("Ais not equal");
		}
		else {
			System.out.println("A is equal to b");
		}

	}

}
